package Day3;

public class Weekdays_switchcase {

	public static void main(String[] args)
	{
		//program for weekdays by using switch case
	String week_day="thursday";
	switch(week_day)
	{
	case "sunday":System.out.println("day_1");break;
	case "monday":System.out.println("day_2");break;
	case "tuesday":System.out.println("day_3");	break;
	case "wednesday":System.out.println("day_4");break;
	case "thursday":System.out.println("day_5");break;
	case "friday":System.out.println("day_6");break;
	case "saturday":System.out.println("day_7");break;
	default: System.out.println("Not valid");
		}

	}

}
